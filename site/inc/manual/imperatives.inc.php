<p>Imperative sentences without an overt subject have category
<code>S[b]\NP</code>, as in CCGrebank. For example:</p>

<div class=der>
	<div class=lexlist>
		<table class=lex>
			<tr>
				<td class="token">Chiamami</td>
			</tr>
			<tr>
				<td class="cat">S[b]\NP</td>
			</tr>
		</table>
		<table class=lex>
			<tr>
				<td class="token">.</td>
			</tr>
			<tr>
				<td class="cat">(S[b]\NP)\(S[b]\NP)</td>
			</tr>
		</table>
	</div>
</div>


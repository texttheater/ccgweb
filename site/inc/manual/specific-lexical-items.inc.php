<h4><i>so</i></h4>

<p>The English adverb <i>so</i> can function as an anaphoric element in
constructions such as <i>do so</i> or <i>think so</i>. In CCGrebank, <i>so</i>
then sometimes appears as an argument of the verb with category
<code>S[adj]\NP</code>, sometimes as a VP modifier with category
<code>(S\NP)\(S\NP)</code>. We opt to treat it as an <code>S[adj]\NP</code>
argument, also in the case of the corresponding Italian adverb <i>così</i>.</p>

<h4><i>too X for Y</i></h4>

<p>In this construction, <i>for Y</i> is analyzed as a PP argument of
<i>too</i>, not of the adjective <i>X</i>. Accordingly with <i>zu X für Y</i>,
<i>te X voor Y</i> and <i>troppo X per Y</i>.</p>


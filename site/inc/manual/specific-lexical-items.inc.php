<h4><i>It’s been X since…</i></h4>

<p>In this English construction, <i>since</i> modifies the verb and is not an
argument or modifier of <i>X</i>.</p>

<h4><i>noch</i>/<i>nog</i></h4>

<p>German <i>noch</i> and Dutch <i>nog</i> as in <i>noch ein N</i>, <i>noch einmal</i>, <i>nog een N</i>
are analyzed as modifying the NP or VP modifier to their right.</p>

